# Data Structures

# Dictionary - represents collections of key-value pairs.
person = {"name": "John", "age": 30}

# Lists - are ordered collections of items, which can 
# be of different types. They are mutable.
numbers = [1, 2, 3, 4, 5]
fruits = ["apple", "banana", "pear"]
mixed_list = [1, "mango", 1.5, False]

# Tuples - similar to lists but are immutable
point = (3, 4)
rgb_color = (255, 0, 0)

# Sets - are unordered collections of unique elements. 
# They are useful for tasks that require handling distinct values.
unique_numbers = {1, 2, 3, 4 , 5}

